const express = require('express');
const pool = require('../db/db');
const { verifyToken } = require('../middleware/auth');
const router = express.Router();

router.use(verifyToken);

const blockAdmin = (req, res, next) => {
  if (req.user.type === 'admin') return res.status(403).json({ message: 'Admins cannot access predictions' });
  next();
};
router.use(blockAdmin);

// GET all predictions (patient sees own; doctor sees assigned patients')
router.get('/', async (req, res) => {
  try {
    let query = `
      SELECT p.*, pt.name as patient_name
      FROM predictions p
      JOIN patients pt ON p.patient_id = pt.id
    `;
    let params = [];
    if (req.user.type === 'patient') {
      query += ' WHERE pt.id = ?';
      params.push(req.user.id);
    } else if (req.user.type === 'doctor') {
      query += ' WHERE pt.doctor_id = ?';
      params.push(req.user.id);
    }
    query += ' ORDER BY p.created_at DESC';
    const [rows] = await pool.query(query, params);
    res.json(rows);
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
});

// GET predictions for a specific patient
router.get('/patient/:patientId', async (req, res) => {
  const { patientId } = req.params;
  try {
    let authorized = false;
    if (req.user.type === 'patient' && parseInt(patientId) === req.user.id) authorized = true;
    else if (req.user.type === 'doctor') {
      const [check] = await pool.query('SELECT id FROM patients WHERE id = ? AND doctor_id = ?', [patientId, req.user.id]);
      if (check.length) authorized = true;
    }
    if (!authorized) return res.status(403).json({ message: 'Access denied' });

    const [rows] = await pool.query('SELECT * FROM predictions WHERE patient_id = ? ORDER BY created_at DESC', [patientId]);
    res.json(rows);
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
});

// No POST/PUT/DELETE – predictions are auto‑generated by medical_records logic

module.exports = router;